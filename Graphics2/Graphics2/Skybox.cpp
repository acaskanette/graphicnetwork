void Skybox::LoadCubeMap(std::vector<const GLchar*> faces) {
	
	glGenTextures(1, &texture0);
	glActiveTexture(GL_TEXTURE0);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_NEAREST); 
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_NEAREST); 
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE); 
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE); 
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);

	for (GLuint i = 0; i < faces.size(); i++) {
		image = SOIL_load_image(faces[i], &width, &height, 0, SOIL_LOAD_RGB);
		glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, image);
	}

	glBindTexture(GL_TEXTURE_CUBE_MAP, 0);
	
}

void Skybox::Render(Camera* camera) {

	glUseProgram(program);				// Set active program

	modelMatrix = glm::mat4(1.0f);
	//viewMatrix = camera->viewMatrix;
	projectionMatrix = glm::perspective(glm::radians(45.0f), 640.0f / 480.0f, 0.1f, 100.0f);
	
	modelMatrixLocation = glGetUniformLocation(program, "modelMatrix");
	viewMatrixLocation = glGetUniformLocation(program, "viewMatrix");
	projectionMatrixLocation = glGetUniformLocation(program, "projectionMatrix");

	glUniformMatrix4fv(modelMatrixLocation, 1, GL_FALSE, glm::value_ptr(modelMatrix));
	glUniformMatrix4fv(viewMatrixLocation, 1, GL_FALSE, glm::value_ptr(camera->viewMatrix));
	glUniformMatrix4fv(projectionMatrixLocation, 1, GL_FALSE, glm::value_ptr(projectionMatrix));
	
	glUniform3f(glGetUniformLocation(program, "cameraPosition"), camera->position.x, camera->position.y, camera->position.z);

	//Draw Cubes
	glBindVertexArray(VAO);
	glBindTexture(GL_TEXTURE_CUBE_MAP, texture0);
	//glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);


	//Draw Skybox
	glDepthFunc(GL_LEQUAL);
	glUseProgram(skyboxProgram);
	
	viewMatrix = glm::mat4(glm::mat3(camera->viewMatrix)); // Trim translation off camera

	//modelMatrixLocation = glGetUniformLocation(skyboxProgram, "modelMatrix");
	viewMatrixLocation = glGetUniformLocation(skyboxProgram, "viewMatrix");
	projectionMatrixLocation = glGetUniformLocation(skyboxProgram, "projectionMatrix");

	//glUniformMatrix4fv(modelMatrixLocation, 1, GL_FALSE, glm::value_ptr(modelMatrix));
	glUniformMatrix4fv(viewMatrixLocation, 1, GL_FALSE, glm::value_ptr(viewMatrix));
	glUniformMatrix4fv(projectionMatrixLocation, 1, GL_FALSE, glm::value_ptr(projectionMatrix));

	glBindVertexArray(skyboxVAO);
	glBindTexture(GL_TEXTURE_CUBE_MAP, texture0);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);

	glDepthFunc(GL_LESS);

}

